/*clicking on the button will toggle btwn hiding/showing the  nav menu*/
function toggleNav() {
    var menu = document.getElementById("dropdownMenu");
    if (menu.style.display === "none") {
        menu.style.display = "block";
    } else {
        menu.style.display = "none";
    }
}




























